    private void BtnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditActionPerformed
        // TODO add your handling code here:
        if (cekKosong()) {
            String id = txKodebuku.getText();
            String judul = txJudulbuku.getText();
            String namaPengarang = txNamapengarang.getText();
            String namaPenerbit = txNamapenerbit.getText();
            String stok = txJumlahstok.getText();
            Buku p = new Buku(id, judul, namaPengarang, namaPenerbit, Integer.parseInt(stok));

            try {
                int jawaban = JOptionPane.showConfirmDialog(this, "Ubah Data " + p.getId_buku()+ "?", "Ubah", JOptionPane.YES_NO_OPTION);
                if (jawaban == JOptionPane.YES_OPTION) {
                if (koneksi.updateBuku(p)) {
                    JOptionPane.showMessageDialog(this, "Berhasil Update");
                    tampilkanBuku();
                    bersihkanForm();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal Update");
                    }
                }
            } catch (SQLException ex) {
                System.out.println("Eksepsi: " + ex.getMessage());
                JOptionPane.showMessageDialog(null, "Exception: " + ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(this, "Pilih Data yang Akan diedit !!!");
        }
    }//GEN-LAST:event_BtnEditActionPerformed

    private void BtnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnHapusActionPerformed
        // TODO add your handling code here:
        if (cekKosong()) {
            int selectedRow = tabelbuku.getSelectedRow();
            String id_buku = tableModel.getValueAt(selectedRow, 0).toString();
            int jawaban = JOptionPane.showConfirmDialog(this, "Hapus Data " + id_buku + "?", "Hapus", JOptionPane.YES_NO_OPTION);
            if (jawaban == JOptionPane.YES_OPTION) {
                //hapus
                try {
                    if (koneksi.deleteBuku(id_buku)) {
                        JOptionPane.showMessageDialog(this, "Berhasil Delete");
                        tampilkanBuku();
                        bersihkanForm();
                    } else {
                        JOptionPane.showMessageDialog(this, "Gagal Delete");
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Eksepsi: " + e.getMessage());
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Pilih Data yang Akan dihapus !!!");
        }
    }//GEN-LAST:event_BtnHapusActionPerformed

