    private void bersihkanForm(){
        txKodebuku.setText(null);
        txJudulbuku.setText(null);
        txNamapengarang.setText(null);
        txNamapenerbit.setText(null);
        txJumlahstok.setText(null);
    }
    
    public Boolean cekKosong(){
        return !(
                txKodebuku.getText().equals("") || 
                txJudulbuku.getText().equals("") || 
                txNamapengarang.getText().equals("")||
                txNamapenerbit.getText().equals("")||
                txJumlahstok.getText().equals(""));
    }
    
    private void tampilkanBuku(){
        tableModel.setRowCount(0);
        try {
            ArrayList<Buku> daftar = koneksi.getBuku();
            for (Buku p : daftar) {
                Object[] isiData = {
                    p.getId_buku(), 
                    p.getJudul(), 
                    p.getNama_pengarang(), 
                    p.getNama_penerbit(),
                    p.getStok()
                };
                tableModel.addRow(isiData);
            }
        } catch (SQLException ex) {
            System.out.println("Eksepsi: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, "Exception: " + ex.getMessage());
        }
    }
    
    private void cariBuku(String cari){
        RowFilter<DefaultTableModel, Object> rowFilter = null;
        if (cari.trim().length() == 0) {
            barisRowSorter.setRowFilter(null);
        }else{
            barisRowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + cari));
        }
    }

