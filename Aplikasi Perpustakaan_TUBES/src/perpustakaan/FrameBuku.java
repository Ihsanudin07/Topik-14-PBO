        String judul = tableModel.getValueAt(selectedRow, 1).toString();
        String namaPengarang = tableModel.getValueAt(selectedRow, 2).toString();
        String namaPenerbit = tableModel.getValueAt(selectedRow, 3).toString();
        String stok = tableModel.getValueAt(selectedRow, 4).toString();

