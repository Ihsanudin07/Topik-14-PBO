    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (cekKosong()) {
            String id = txKodebuku.getText();
            String judul = txJudulbuku.getText();
            String namaPengarang = txNamapengarang.getText();
            String namaPenerbit = txNamapenerbit.getText();
            String stok = txJumlahstok.getText();
            Buku p = new Buku(id, judul, namaPengarang, namaPenerbit, Integer.parseInt(stok));

            try {
                if (koneksi.insertBuku(p)) {
                    JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan");
                    tampilkanBuku();
                    bersihkanForm();
                } else {
                    JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan");
                }
            } catch (SQLException ex) {
                System.out.println("Eksepsi: " + ex.getMessage());
                JOptionPane.showMessageDialog(null, "Exception: " + ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(this, "Masukkan Data Buku");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

