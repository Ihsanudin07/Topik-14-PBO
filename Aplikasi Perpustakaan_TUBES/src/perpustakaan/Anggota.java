    private void bersihkanForm(){
        txId.setText(null);
        txNama.setText(null);
        cbKelas.setSelectedIndex(0);
        cbJeniskelamin.setSelectedIndex(0);
        txAlamat.setText(null);
        txTtl.setText(null);
    }
    
    private void tampilkan(){
        tableModel.setRowCount(0);
        try {
            ArrayList<AnggotaPojo> daftar = koneksi.getAnggotaPojo();
            for (AnggotaPojo p : daftar) {
                Object[] isiData = {
                    p.getId_anggota(), 
                    p.getNama(), 
                    p.getKelas(), 
                    p.getJenis_Kelamin(),
                    p.getAlamat(), 
                    p.getTtl()
                };
                tableModel.addRow(isiData);
            }
        } catch (SQLException ex) {
            System.out.println("Eksepsi: " + ex.getMessage());
            JOptionPane.showMessageDialog(null, "Exception: " + ex.getMessage());
        }
    }
    
    private void cariData(String cari){
        RowFilter<DefaultTableModel, Object> rowFilter = null;
        if (cari.trim().length() == 0) {
            barisRowSorter.setRowFilter(null);
        }else{
            barisRowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + cari));
        }
    }
    
    public Boolean cekKosong(){
        return !(
                txId.getText().equals("") || 
                txNama.getText().equals("") || 
                cbKelas.getSelectedItem().toString().equals("") ||
                cbJeniskelamin.getSelectedItem().toString().equals("") ||
                txAlamat.getText().equals("")||
                txTtl.getText().equals(""));
    }
    
