    private void simpanbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanbtnActionPerformed
        // TODO add your handling code here:
        if (cekKosong()) {
            String id = txId.getText();
            String nama = txNama.getText();
            String kelas = cbKelas.getSelectedItem().toString();
            String jenis = cbJeniskelamin.getSelectedItem().toString();
            String alamat = txAlamat.getText();
            String ttl = txTtl.getText();
            AnggotaPojo p = new AnggotaPojo(id, nama, kelas, jenis, alamat, ttl);

            try {
                if (koneksi.insertAnggota(p)) {
                    JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan");
                    tampilkan();
                    bersihkanForm();
                } else {
                    JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan");
                }
            } catch (SQLException ex) {
                System.out.println("Eksepsi: " + ex.getMessage());
                JOptionPane.showMessageDialog(null, "Exception: " + ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(this, "Masukkan Data Anggota");
        }
    }//GEN-LAST:event_simpanbtnActionPerformed

